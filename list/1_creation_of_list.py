# Empty List
empty_list = []
print(empty_list)
# Output: []

# List with elements
# List of integers
int_list = [1, 2, 3, 4, 5]
print(int_list)
# Output: [1, 2, 3, 4, 5]

# List of strings
str_list = ["apple", "banana", "cherry"]
print(str_list)
# Output: ["apple", "banana", "cherry"]

# List with mixed data types
mixed_list = [1, "Hello", 3.14, True]
print(mixed_list)
# Output: [1, "Hello", 3.14, True]

# Nested list
nested_list = [1, [2, 3], [4, 5]]
print(nested_list)
# Output: [1, [2, 3], [4, 5]]
